package com.jetbrains;

public class KnickKnack {

    public static void rollingHome()
    {
        System.out.println("With a knick-knack paddywhack,");
        System.out.println("Give the dog a bone,");
        System.out.println("This old mand came rolling home.");
    }

    public static void main(String[] args)
    {
        System.out.println("This old man, he played one.");
        System.out.println("He played knick-knack on my thumb;");
        rollingHome();

        System.out.println("\nThis old man, he played two.");
        System.out.println("He played knick-knack on my shoe;");
        rollingHome();

        System.out.println("\nThis old man, he played three.");
        System.out.println("He played knick-knack on my knee;");
        rollingHome();

        System.out.println("\nThe old man, he played four.");
        System.out.println("He played knick-knack on my door;");
        rollingHome();

        System.out.println("\nThis old man, he played five.");
        System.out.println("He played knick-knack on my hive;");
        rollingHome();

        System.out.println("\nThis old man, he played six,");
        System.out.println("He played knick-knack on my sticks;");
        rollingHome();
    }
}
